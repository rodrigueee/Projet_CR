    private void btokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btokActionPerformed
        jLabel1.setBackground(Color.white);
        jLabel2.setBackground(Color.white);
        jLabel3.setBackground(Color.white);
        boolean erreur = false;
        String nom = jLabel1.getText();
        if (nom.trim().equals("")) {
            erreur = true;
            jLabel1.setBackground(Color.red);
        }
        String adresse = jLabel2.getText();
        if (adresse.trim().equals("")) {
            erreur = true;
            jLabel2.setBackground(Color.red);
        }
        String tel = jLabel3.getText();
        if (tel.trim().equals("")) {
            erreur = true;
            jLabel3.setBackground(Color.red);
        }

        if (!erreur) {
            Client cl = new Client(nom, adresse, tel);
            String msg = pjm.ajouterClient(cl);
            JOptionPane.showMessageDialog(this, msg, "RÃ©sultat", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btokActionPerformed

